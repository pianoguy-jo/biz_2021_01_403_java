package com.callor.var;

public class Var_03 {

	public static void main(String args[]) {
		
		int intNum1 = 0;
		
		intNum1 = 30;
		System.out.println(intNum1);
		
		System.out.println(intNum1 + 40);
		
		int intNum2 = 0;
		intNum2 = 40;
		
		
		// intNum1변수 값을 읽어 오고
		// intNum2변수 값을 읽어 오고
		// 두 값을 더하라.
		System.out.println(intNum1 + intNum2);
		
		int intSum = 0;
		
		// intNum1변수 값을 읽어 오고 intNum2 변수값을 읽어 와서
		// 두 값을 덧셈한 후 위에서 선언하고 초기화 해 둔
		// intSum 변수에 저장하라
		intSum = intNum1 + intNum2;
		// 그리고 intSum 변수에 저장된 값을 읽어서 Console에 출력하라.
		
		System.out.println(intSum);
		
		
	}
}
