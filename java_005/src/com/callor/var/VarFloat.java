package com.callor.var;

public class VarFloat {
/*
 * 실수를 표현하기 2
 * float(소수점 6자리까지 표현) 
 */
	
	public static void main(String args[]) {
		
		/*
		 * float형(단정도실수형) 변수 num1을 선언하고 float형 값 50.0을 저장하라
		 * 이때 숫자값에는 F(f)를 붙여야 한다
		 */
		float num1 = 50.0F;
		float num2 = 50.111111111111111111111f;
		
		System.out.println(num1);
		System.out.println(num2);
	}
}
