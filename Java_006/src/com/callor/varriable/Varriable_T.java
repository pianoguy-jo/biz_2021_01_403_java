package com.callor.varriable;

public class Varriable_T {

	public static void main(String args[]) {
		
		// 변수를 선언과 초기화 하기
		int num1 = 0;
		int num2 = 0;
		
		// 변수값을 선언하기
		num1 = 30;
		num2 = 40;
		
		
		// 출력할 변수를 초기화하기
		int result1 = 0;
		int result2 = 0;
		int result3 = 0;
		int result4 = 0;
		int result5 = 0;
		
		
		//연산결과를 변수에 저장하기
		result1 = num1 + num2;
		result2 = num1 - num2;
		result3 = num1 * num2;
		result4 = num1 / num2;
		result5 = num1 % num2;
						
		// 변수에 저장된 결과를 Console에 출력하기
		System.out.println(result1);
		System.out.println(result2);
		System.out.println(result3);
		System.out.println(result4);
		System.out.println(result5);		
	}
}
